// コード → 業種の逆引きマップを構築
const CODE_TO_INDUSTRY = {}
for (const [industry, codes] of Object.entries(GROUPS)) {
  for (const code of codes) {
    if (!CODE_TO_INDUSTRY[code]) CODE_TO_INDUSTRY[code] = industry
  }
}

export function getSameIndustryRecommendations(ticker) {
  const code = ticker.replace(/\.T$/i, '')
  const industry = CODE_TO_INDUSTRY[code]
  if (!industry) return null

  const stocks = GROUPS[industry]
    .filter(c => c !== code)
    .map(c => ({ ticker: c + '.T', name: JP_NAMES[c] || null }))
    .slice(0, 4)

  return { industry, stocks }
}
